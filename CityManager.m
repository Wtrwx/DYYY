    if (!code || code.length < 6) {
        return nil;
    }
    NSString *provinceCode = [code substringToIndex:2];
    provinceCode = [provinceCode stringByAppendingString:@"0000"];
    NSString *provinceCodeName = self.cityCodeMap[provinceCode];
    
    return provinceCodeName;
